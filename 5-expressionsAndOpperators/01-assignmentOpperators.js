//assignment operator
let r= 5;

//additional assignment
r += 1; // r= r +1

//subtraction assignment
r -= 1; // r = r- 1

//multiplication assignment
r *= 1; // r = r * 1

//division assingment
r /= 1; // r = r / 1

//remainder ( or modulus) assingment. helps see if something can be divided evenly or if there will be a remander.
r %= 1; // r = r % 1;

//exponential assignment
r **= 1; // r = r ** 1
